#include "minishell.h"

int	find_plase(char *s, char c)
{
	int	n;

	n = 0;
	if (!s)
		return (0);
	while (s[n] && s[n] != c)
		n++;
	if (s[n] == c)
		;
	else
		n++;
	return (n);
}
